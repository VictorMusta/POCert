const CACHE_NAME = "pocert-static-v1";
const ASSETS = [
  "./",
  "./index.html",
  "./css/base.css",
  "./css/components.css",
  "./css/pages.css",
  "./js/data.js",
  "./js/theme.js",
  "./js/main.js",
];

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(ASSETS)).catch(() => Promise.resolve())
  );
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(
          keys.map((key) => {
            if (key !== CACHE_NAME) {
              return caches.delete(key);
            }
            return undefined;
          })
        )
      )
      .catch(() => Promise.resolve())
  );
});

self.addEventListener("fetch", (event) => {
  if (event.request.method !== "GET") return;
  event.respondWith(
    caches.match(event.request).then((cached) => cached || fetch(event.request)).catch(() => fetch(event.request))
  );
});
